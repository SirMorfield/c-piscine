int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int				var;
	int				var1;
	unsigned int	i;

	i = 0;
	while (*s1 == *s2 && *s1 && *s2 && i < n)
	{
		s1++;
		s2++;
		i++;
	}
	var = (unsigned char)*s1;
	var1 = (unsigned char)*s2;
	return (var - var1);
}
